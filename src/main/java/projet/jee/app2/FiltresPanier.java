package projet.jee.app2;

public class FiltresPanier {

	private String commercant;
	private String categorie;
	private String localisationPanier;
	
	public String getCommercant() {
		return commercant;
	}
	public void setCommercant(String commercant) {
		this.commercant = commercant;
	}
	public String getCategorie() {
		return categorie;
	}
	public void setCategorie(String categorie) {
		this.categorie = categorie;
	}
	public String getLocalisationPanier() {
		return localisationPanier;
	}
	public void setLocalisationPanier(String localisationPanier) {
		this.localisationPanier = localisationPanier;
	}
	
}
